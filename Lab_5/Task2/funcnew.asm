; Функция преобразования строки в число
; Принимает: rsi - указатель на строку
; Возвращает: rax - число
str_number:
  xor rax, rax        ; обнуляем результат
  xor rbx, rbx        ; обнуляем rbx (буфер для цифры)
  .loop:
    mov bl, [rsi]     ; загружаем символ
    cmp bl, 0         ; проверяем на конец строки
    je .end           ; если конец, выходим
    cmp bl, '0'       ; проверяем, что символ - цифра
    jl .end           ; если меньше '0', выходим
    cmp bl, '9'       ; проверяем, что символ - цифра
    jg .end           ; если больше '9', выходим
    sub bl, '0'       ; преобразуем символ в цифру
    imul rax, rax, 10 ; умножаем результат на 10
    add rax, rbx      ; добавляем цифру к результату
    inc rsi           ; переходим к следующему символу
    jmp .loop         ; продолжаем цикл
  .end:
    ret               ; возвращаемся

; Функция выхода из программы
; Принимает: нет
; Возвращает: нет
exit:
  mov rax, 60         ; номер системного вызова exit
  mov rdi, 0          ; код возврата
  syscall             ; вызываем системный вызов
